
# 数值类型
# 数值分为三种:整数、浮点、负数
# python中的整数大小没有限制，可以是无限大的整数
long_number = 9999999999999999999999999999999999999999999
print(long_number)

# 如果数字的长度过大，可以使用下划线做完分隔符
long_number = 9999999_9999999_9999999_9999999_99999999_9999999
print(long_number)

# 其他进制整数标识
# 10进制不可以使用 0 开头
# 二进制 0b 开头
number = 0b10
print(number)
# 八进制 0o 开头
number = 0o10
print(number)
# 16进制 0x 开头
number = 0x10
print(number)

# 运算符
number = 100
number += 10
print(number)

# 浮点(float)
money = 0.1
print(money)
